-- 初始化景区统计数据 (scenic_statistics)
-- 为每个景区生成最近90天的数据

-- 先清空旧数据
DELETE FROM scenic_statistics WHERE stat_date >= DATE_SUB(CURDATE(), INTERVAL 90 DAY);

-- 为景区ID 1-5 插入数据
INSERT INTO scenic_statistics (scenic_id, stat_date, visitor_count, order_count, revenue, favorites_count, avg_stay_time, weather, is_weekend, is_holiday, created_at)
SELECT 
    scenic.id as scenic_id,
    DATE_SUB(CURDATE(), INTERVAL seq.seq DAY) as stat_date,
    -- 游客量：根据景区热度调整，周末更高
    CASE 
        WHEN DAYOFWEEK(DATE_SUB(CURDATE(), INTERVAL seq.seq DAY)) IN (1, 7) 
        THEN FLOOR(300 + RAND() * 500 + scenic.base_visitors * 0.3)
        ELSE FLOOR(150 + RAND() * 300 + scenic.base_visitors * 0.2)
    END as visitor_count,
    -- 订单量
    CASE 
        WHEN DAYOFWEEK(DATE_SUB(CURDATE(), INTERVAL seq.seq DAY)) IN (1, 7) 
        THEN FLOOR((300 + RAND() * 500) * 0.6)
        ELSE FLOOR((150 + RAND() * 300) * 0.6)
    END as order_count,
    -- 收入
    CASE 
        WHEN DAYOFWEEK(DATE_SUB(CURDATE(), INTERVAL seq.seq DAY)) IN (1, 7) 
        THEN FLOOR((300 + RAND() * 500) * (60 + RAND() * 40))
        ELSE FLOOR((150 + RAND() * 300) * (60 + RAND() * 40))
    END as revenue,
    -- 收藏数
    FLOOR(5 + RAND() * 20) as favorites_count,
    -- 平均停留时间 2-4小时
    ROUND(2 + RAND() * 2, 1) as avg_stay_time,
    -- 天气
    ELT(FLOOR(1 + RAND() * 4), '晴', '多云', '阴', '小雨') as weather,
    -- 是否周末
    CASE WHEN DAYOFWEEK(DATE_SUB(CURDATE(), INTERVAL seq.seq DAY)) IN (1, 7) THEN 1 ELSE 0 END as is_weekend,
    -- 是否节假日
    0 as is_holiday,
    NOW() as created_at
FROM (
    SELECT 1 as id, 500 as base_visitors UNION
    SELECT 2, 400 UNION
    SELECT 3, 350 UNION
    SELECT 4, 300 UNION
    SELECT 5, 250
) scenic
