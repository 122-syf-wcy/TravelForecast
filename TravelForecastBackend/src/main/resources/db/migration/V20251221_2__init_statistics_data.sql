-- 初始化平台统计数据 (platform_statistics)
-- 生成最近90天的数据

-- 先清空旧数据
DELETE FROM platform_statistics WHERE stat_date >= DATE_SUB(CURDATE(), INTERVAL 90 DAY);

-- 插入最近90天的平台统计数据
INSERT INTO platform_statistics (stat_date, total_users, total_merchants, total_scenics, total_revenue, daily_visitors, daily_orders, daily_revenue, created_at)
SELECT 
    DATE_SUB(CURDATE(), INTERVAL seq.seq DAY) as stat_date,
    -- 总用户数：基础18人，每天增加0-2人
    18 + FLOOR(RAND() * 2 * (90 - seq.seq)) as total_users,
    -- 总商户数：固定5个
    5 as total_merchants,
    -- 总景区数：固定5个
    5 as total_scenics,
    -- 总收入：累计
    250000 + FLOOR(RAND() * 50000) + (90 - seq.seq) * 3000 as total_revenue,
    -- 日游客量：周末1500-2500，工作日800-1500
    CASE 
        WHEN DAYOFWEEK(DATE_SUB(CURDATE(), INTERVAL seq.seq DAY)) IN (1, 7) 
        THEN 1500 + FLOOR(RAND() * 1000)
        ELSE 800 + FLOOR(RAND() * 700)
    END as daily_visitors,
    -- 日订单量：游客量的50-70%
    CASE 
        WHEN DAYOFWEEK(DATE_SUB(CURDATE(), INTERVAL seq.seq DAY)) IN (1, 7) 
        THEN FLOOR((1500 + FLOOR(RAND() * 1000)) * (0.5 + RAND() * 0.2))
        ELSE FLOOR((800 + FLOOR(RAND() * 700)) * (0.5 + RAND() * 0.2))
    END as daily_orders,
    -- 日收入：人均80-120元
    CASE 
        WHEN DAYOFWEEK(DATE_SUB(CURDATE(), INTERVAL seq.seq DAY)) IN (1, 7) 
        THEN (1500 + FLOOR(RAND() * 1000)) * (80 + FLOOR(RAND() * 40))
        ELSE (800 + FLOOR(RAND() * 700)) * (80 + FLOOR(RAND() * 40))
    END as daily_revenue,
    NOW() as created_at
FROM (
